#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	printf("Size of char data type:%d byte(s)\n", sizeof(char));
	printf("Size of int data type:%d byte(s)\n", sizeof(int));
	printf("Size of long int data type:%d byte(s)\n", sizeof(long int));
	printf("Size of long long int data type:%d byte(s)\n", sizeof(long long int));
	printf("Size of float data type:%d byte(s)\n", sizeof(float));
	return (0);
}
