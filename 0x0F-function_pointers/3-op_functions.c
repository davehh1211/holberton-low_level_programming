#include "3-calc.h"

/**
 * op_add - addition of integers
 * @a: value to add
 * @b: value to add
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction of integers
 * @a: value to subtract
 * @b: value to subtracted
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of integers
 * @a: value to multiply
 * @b: value to multiply
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of integers
 * @a: value to divide
 * @b: value to divide
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module of integers
 * @a: value to add
 * @b: value to add
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
